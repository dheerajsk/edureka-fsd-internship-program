// String
var str = "Dheeraj";
console.log(typeof str);
console.log(str);

// number
var num = 10;
console.log(num);
console.log(typeof num);

// boolean
var isNumber = true;
console.log(isNumber);
console.log(typeof isNumber);

// undefined
var value;
console.log(value);
console.log(typeof value);

// object
var obj ={name:"Dheerj", dept: "IT"};
console.log(obj);
console.log(typeof obj);

// Date
var date = new Date();
console.log(date);
console.log(typeof date);
